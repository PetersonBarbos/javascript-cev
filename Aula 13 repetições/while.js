//while é estrutura de repetição com teste lógico no ínicio
var c = 1
while(c <= 30){
    console.log(`passo ${c} no while`)
    c++
}

//a estrutura que usa teste lógico no final é o do... while
c = 1
do{
    console.log(`Agora passo ${c} no do...while`)
    c++
} while(c<=6)

for(var c=2;c<=10;c++){
    console.log(`Agora passo ${c} no for`)
}